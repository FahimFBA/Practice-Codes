// Compute the area of a circle 

class AreaOfCircle{
    public static void main(String[] args){
        double pi, r,a;
        r = 10.8; // radius of circle
        pi = 3.1416; // pi, approximately 
        a = pi * r * r; // Compute area of the circle

        System.out.println("Area of the circle is " + a);
    }
}